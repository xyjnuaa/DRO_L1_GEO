function dX = cr3bp_rhs(~, X, mu)
%==========================================================================
% 函数名称：cr3bp_rhs
% 功能说明：
%   给出地月圆限制三体问题（CR3BP）在旋转坐标系下的运动方程右端项。
%
% 输入：
%   X  - 状态向量 [x; y; z; vx; vy; vz]
%   mu - 月球质量比
%
% 输出：
%   dX - 状态导数
%
% 说明：
%   本函数中的状态量是“无量纲”的，单位与地月CR3BP标准化一致。
%==========================================================================

x  = X(1);
y  = X(2);
z  = X(3);
vx = X(4);
vy = X(5);
vz = X(6);

r1 = sqrt((x + mu)^2     + y^2 + z^2); % 到地球主星的距离
r2 = sqrt((x - 1 + mu)^2 + y^2 + z^2); % 到月球主星的距离

ax = 2*vy + x - (1-mu)*(x+mu)/r1^3 - mu*(x-1+mu)/r2^3;
ay = -2*vx + y - (1-mu)*y/r1^3      - mu*y/r2^3;
az = -(1-mu)*z/r1^3 - mu*z/r2^3;

dX = [vx; vy; vz; ax; ay; az];

end
