function const = get_constants()
%==========================================================================
% 函数名称：get_constants
% 功能说明：
%   统一给出本工程中会用到的常数。
%
% 说明：
%   这里的常数主要分为两类：
%   1) 地月CR3BP（圆限制三体问题）传播会用到的常数
%   2) 简化GEO传播会用到的地球引力常数、同步轨道半径等
%==========================================================================

%---------------------- 地月CR3BP相关常数 ----------------------
const.mu = 1.215058560962404e-2;     % 月球质量比 μ = m_Moon / (m_Earth + m_Moon)
const.LU_km = 384400;                % 地月平均距离，作为长度单位LU（km）
const.TU_s = 375190.25852;           % 时间单位TU（秒），约等于 1/n_EM
const.n_EM = 1 / const.TU_s;         % 地月平均角速度（rad/s）
const.omega_EM = const.n_EM;         % 旋转系角速度（rad/s）
const.vu_kms = const.LU_km / const.TU_s; % 速度单位（km/s）

%---------------------- 地球/GEO相关常数 ----------------------
const.muE_km3s2 = 398600.4418;       % 地球引力常数 μ_E （km^3/s^2）
const.geo_a_km = 42164.0;            % 理想同步圆轨道半长轴（km）
const.earth_radius_km = 6378.1363;   % 地球平均半径（km）
const.c_km_s = 299792.458;           % 光速（km/s）
const.wE_rad_s = 7.2921159e-5;       % 地球自转角速度（rad/s）

end
